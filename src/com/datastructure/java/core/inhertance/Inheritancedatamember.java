package com.datastructure.java.core.inhertance;

public class Inheritancedatamember {
    public int a;
    public int b;

    public Inheritancedatamember(int a, int b) {
        this.a = a;
        this.b = b;
    }

    @Override
    public String toString() {
        return "Inheritancedatamember{" +
                "a=" + a +
                ", b=" + b +
                '}';
    }
}
